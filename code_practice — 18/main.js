let frand = document.getElementById("frand"); //кнопка играть с другом
let robot = document.getElementById("robot"); //кнопка играть с роботом
let info = document.getElementById("info"); //параграф выводящий информацию о ходе игры

let called = []; //массив с городами которые уже были озвучены
let switcher = undefined; //переключатель игроков
let first; //первая буква города
