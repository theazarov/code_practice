frand.addEventListener("click", function go() {
  //нажимаем на кнопку играть с другом
  info.innerHTML = "отныне вы не друзья, а враги! введите название города: ";

  frand.classList.add("hidden"); //скрываем кнопку с экрана
  robot.classList.add("hidden"); //скрываем кнопку с экрана

  let inputOne = document.createElement("input"); //создаем новый инпут для первого игрока
  let paragraph_first_player = document.createElement("p"); //создаем параграф для первого игрока
  paragraph_first_player.innerHTML = "игрок №1 "; //добавляем текст в параграф первого игрока
  paragraph_first_player.appendChild(inputOne); //добавляем в конец параграфа инпут с полем ввода
  parent.appendChild(paragraph_first_player); //добавляем параграф содержащий инпут в конец родительского элемента

  let inputTwo = document.createElement("input"); //создаем новый инпут для второго игрока
  let paragraph_second_player = document.createElement("p"); //создаем параграф для второго игрока

  let defeat = document.createElement("button"); //создаем кнопку завершения игры
  defeat.innerHTML = "Я сдаюсь"; //добавляем кнопке текст

  inputOne.addEventListener("keydown", function (event) {
    //нажимая в поле ввода первого игрока клавишу enter запускаем функцию
    if (event.keyCode == 13 && switcher == undefined) {
      //если был нажат enter и переключатель был на undefined
      inputOne.value = inputOne.value.toUpperCase();
      called.push(inputOne.value); //добавляем из инпута в пустой массив с городами первый город

      last = inputOne.value.split("").pop(); //вырезаем последнюю букву в слове
      if (last == "Ь" || last == "Ы") {
        //если последняя буква была 'Ь'
        last = inputOne.value.split("").splice(-2, 1); //тогда вместо нее вырезаем предпоследнюю букву
      }

      info.innerHTML =
        "город " +
        "<b>" +
        inputOne.value +
        "</b>" +
        ", назовите город на букву " +
        "<b>" +
        last +
        "</b>"; //выводим информацио о том какой город введен и его последнюю букву

      paragraph_second_player.innerHTML = "игрок №2 "; //добавляем текст в параграф второго игрока
      paragraph_second_player.appendChild(inputTwo); //добавляем в параграф второго игрока инпут для ввода
      parent.appendChild(paragraph_second_player); //добавляем параграф второго игрока в родительский елемент
      inputOne.classList.add("hidden"); //скрываем инпут первого игрока

      inputTwo.focus(); //делаем фокус на инпуте второго игрока
      friendsGame(inputTwo, inputOne);
      friendsGame(inputOne, inputTwo); //поочередно вызываем функцию игры меняя местами инпуты игроков
      switcher = 2; //меняем значение переключателя закрывая доступ к этому коду, теперь игра выполняется в функции
    }
  });
  parent.insertAdjacentElement("afterEnd", defeat); //добавляем после родительского елемента кнопку завершения игры

  defeat.addEventListener("click", function () {
    //нажимая на кнопку завершения игры запускаем следующую функцию
    switcher == 2
      ? (info.innerHTML = "победил №1")
      : (info.innerHTML = "победил №2"); //определяем с помощью переключателя кто из игроков победил
    defeat.innerHTML = "начать заново"; //даем новое название для кнопки завершение - назвав ее начать заново
    paragraph_first_player.classList.add("hidden"); //скрываем инпут первого игрока
    paragraph_second_player.classList.add("hidden"); //скрываем инпут второго игрока

    defeat.addEventListener("click", function () {
      //нажимая на кнопку начать занова запускаем следующую функцию
      frand.classList.remove("hidden"); //показываем кнопку игры с другом которая была спрятана
      robot.classList.remove("hidden"); //показываем кнопку игры с роботом которая была спрятана
      defeat.classList.add("hidden"); //скрываем кнопку (начать заново)
      info.innerHTML = ""; //очищаем параграф с информацией
      called = []; //очищаем масив с городами которые уже были озвучины
      switcher = undefined; //присваиваем переключателю снова значение undefined

      frand.addEventListener("click", go); //теперь при новом клике на кнопку игры с другом запускается новая игра
    });
  });
});

function friendsGame(player_input, enemy_player) {
  //первое значение функции это инпут игрока, второе это инпут его противника

  player_input.addEventListener("keydown", function (event) {
    //нажимая кнопку в инпуте игрока запускаем функцию
    player_input.value = player_input.value.toUpperCase();
    first = player_input.value.split("").shift(); //вырезаем первую букву в слове

    if (
      event.keyCode == 13 &&
      first == last &&
      !called.includes(player_input.value)
    ) {
      //если был нажат ентер и первая буква введенного слова совпадает с последней буквой предыдущего слова и такое слово отсутствует в массиве
      called.push(player_input.value); //тогда добавляем введенное слово в массив
      last = player_input.value.split("").pop(); //и обновляем значение последней буквы, взяв его уже с нового слова

      if (last == "Ь" || last == "Ы") {
        //если последняя буква равна мягкому знаку
        last = player_input.value.split("").splice(-2, 1); //тогда берем предпоследнюю букву из слова
      }
      info.innerHTML =
        "город " +
        "<b>" +
        player_input.value +
        "</b>" +
        ", назовите город на букву " +
        "<b>" +
        last +
        "</b>"; //обновляем информацию о введенном слове

      enemy_player.value = ""; //очищаем инпут врага от текста
      player_input.classList.add("hidden"); //скрываем инпут игрока
      enemy_player.classList.remove("hidden"); //и показываем инпут для ввода следующему игроку

      switcher == 2 ? (switcher = 1) : (switcher = 2); //меняем значения переключателя при каждом вызове функции
      enemy_player.focus(); //добавляем фокус на инпуте врага
    } else if (
      event.keyCode == 13 &&
      first == last &&
      called.includes(player_input.value)
    ) {
      //проверяем если такой город уже был назван
      info.innerHTML = "такой город уже был"; //выводим об этом информацию
    } else if (event.keyCode == 13 && first != last) {
      //проверяем если первая буква слова не равна последней букве предыдущего слова
      info.innerHTML = "первой буквой должна быть " + "<b>" + last + "</b>"; //выводим об этом информацию
    }
  });
}

